    @Override
    public void setForm(Model.DatBan entity) {
        txtMaDat.setText(entity.getMaDat());
        txtMaKH.setText(entity.getMaKH());
        cboMaBan.setSelectedItem(entity.getMaBan());
        txtNgayDat.setDate(entity.getNgayDat());

        // ✅ Chuyển chuỗi giờ (HH:mm) sang LocalTime rồi set vào TimePicker
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("HH:mm");
        LocalTime time = LocalTime.parse(entity.getGioDat().toString(), formatter);
        tPGioDat.setTime(time);  // tPGioDat là biến kiểu TimePicker

        txtSoNguoi.setText(String.valueOf(entity.getSoNguoi()));
        txtTrangThai.setText(entity.getTrangThai());
    }
    

    @Override
    public DatBan getForm() {
        String maDat = txtMaDat.getText().trim();
    String maKH = txtMaKH.getText().trim();
    String maBan = cboMaBan.getSelectedItem().toString().trim();

    Date ngayDat = txtNgayDat.getDate();

    // Lấy giờ từ TimePicker, format "HH:mm"
    LocalTime gio = tPGioDat.getTime();
    String gioDat = (gio != null) ? gio.format(DateTimeFormatter.ofPattern("HH:mm")) : "";

    String soNguoi = txtSoNguoi.getText().trim();
    String trangThai = txtTrangThai.getText().trim();

    return new DatBan();
    }

    @Override
    public void fillToTable() {
        DefaultTableModel model = (DefaultTableModel) tblDatBan.getModel();
        model.setRowCount(0);
        items = dao.findAll();
        items.forEach(item -> {
            Object[] rowData = {
                item.getMaDat(),
                item.getMaKH(),
                item.getMaBan(),
                item.getNgayDat(),
                item.getGioDat(),
                item.getSoNguoi(),
                item.getTrangThai()
            };
            model.addRow(rowData);
        });
    }

    @Override
    public void edit() {
        DatBan entity = items.get(tblDatBan.getSelectedRow());
        this.setForm(entity);
    }

    @Override
    public void create() {
        if (!Checkall()) return; 
        
        if (UDialog.confirm("Bạn thực sự muốn thêm nhân viên này?")) {
            DatBan entity = getForm();
            dao.create(entity);
            this.fillToTable();
            this.clear();
            UDialog.alert("Đã thêm thành công");
        }
    }

