    public void update() {
        if (!Checkall()) return; 
        
        if (UDialog.confirm("Bạn thực sự muốn thêm nhân viên này?")) {
            DatBan entity = getForm();
            dao.update(entity);
            this.fillToTable();
            this.clear();
            UDialog.alert("Đã thêm thành công");
        }
    }

    @Override
    public void delete() {
    int row = tblDatBan.getSelectedRow();
    if (row == -1) {
        UDialog.alert("Vui lòng chọn nhân viên cần xóa!");
        return;
    }

    String maDat = tblDatBan.getValueAt(row, 0).toString();
    if (UDialog.confirm("Bạn chắc chắn muốn xóa nhân viên này?")) {
        dao.deleteById(maDat);
        this.fillToTable();
        this.clear();
        UDialog.alert("Xóa nhân viên thành công!");
    }
    }

    @Override
    public void clear() {
        this.setForm(new DatBan());
    }

    @Override
    public void moveFirst() {
        int currentIndex = tblDatBan.getSelectedRow();
        if (currentIndex == 0) {
            UDialog.alert( "Đã ở vị trí đầu tiên rồi!");
        } else {
            this.moveTo(0);
        }
    }

    @Override
    public void movePrevious() {
        this.moveTo(tblDatBan.getSelectedRow() - 1);
    }

    @Override
    public void moveNext() {
        this.moveTo(tblDatBan.getSelectedRow() + 1);
    }

    @Override
    public void moveLast() {
        this.moveTo(tblDatBan.getRowCount() - 1);
    }

    @Override
    public void moveTo(int rowIndex) {
        if (rowIndex < 0) {
        this.moveLast();
    } else if (rowIndex >= tblDatBan.getRowCount()) {
        this.moveFirst();
    } else {
        tblDatBan.clearSelection();
        tblDatBan.setRowSelectionInterval(rowIndex, rowIndex);
        this.edit();
    }
    }

    @Override
    public boolean Checkall() {
       if (txtMaDat.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Chưa nhập mã đặt bàn");
        txtMaDat.requestFocus();
        return false;
    }
    if (txtMaKH.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Chưa nhập mã khách hàng");
        txtMaKH.requestFocus();
        return false;
    }
   if (cboMaBan.getSelectedItem() == null || cboMaBan.getSelectedItem().toString().trim().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Chưa chọn mã bàn");
    cboMaBan.requestFocus();
    return false;
}

    if (txtSoNguoi.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Chưa nhập số người");
        txtSoNguoi.requestFocus();
        return false;
    }
    if (txtTrangThai.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Chưa nhập trạng thái");
        txtTrangThai.requestFocus();
        return false;
    }
    if (txtNgayDat.getDate() == null) {
        JOptionPane.showMessageDialog(this, "Chưa chọn ngày đặt");
        txtNgayDat.requestFocus();
        return false;
    }
    if (tPGioDat.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Chưa chọn giờ đặt");
        tPGioDat.requestFocus();
        return false;
    }

    return true;
    }
