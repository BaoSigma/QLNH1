    @Override
    public void setForm(Model.HoaDon entity) {
         txtmahoadon.setText(entity.getMaHD());
         txtTenKhach.setText(entity.getTenKH());
    txtmanv.setText(UAuth.user.getMaNV());
    txtmaban.setText(entity.getMaBan());
    txttongtien.setText(String.valueOf(entity.getTongTien()));
    dcNgayLap.setDate(entity.getNgayLap());
    String httt = entity.getHinhThucTT();
    if ("Tiền mặt".equals(httt)) {
        cboHTTT.setSelectedItem("Tiền mặt");
    } else {
        cboHTTT.setSelectedItem("Chuyển khoản");
    }

    }

    @Override
    public Model.HoaDon getForm() {
             HoaDon hd = new HoaDon();
    HoaDonImpl vtDao = new HoaDonImpl();
    hd.setTenKH(txtTenKhach.getText().trim());
    hd.setMaHD(txtmahoadon.getText().trim());
    hd.setMaNV(txtmanv.getText().trim());
    hd.setMaBan(txtmaban.getText().trim());
    hd.setTongTien(Double.parseDouble(txttongtien.getText().trim()));
    hd.setNgayLap(dcNgayLap.getDate());
    String httt = cboHTTT.getSelectedItem().toString();
    hd.setHinhThucTT(httt);
    return hd;
    }

    @Override
    public void fillToTable() {
         DefaultTableModel model = (DefaultTableModel) tblhoadon.getModel();
        model.setRowCount(0);
        items = dao.findAll();
        items.forEach(item -> {
            Object[] rowData = {
                item.getMaHD(),
                item.getMaBan(),
                item.getMaNV(),
                item.getNgayLap(),
                item.getTongTien(),
                item.getHinhThucTT(),
                item.getTenKH()
            };
            model.addRow(rowData);
        });
    }

    @Override
    public void edit() {
        HoaDon entity = items.get(tblhoadon.getSelectedRow());
        this.setForm(entity);
    }

    @Override
    public void create() {
        if (!Checkall()) return; 

        if (UDialog.confirm("Bạn thực sự muốn thêm?")) {
            HoaDon entity = this.getForm();
            dao.create(entity);
            this.fillToTable();
            this.clear();
            UDialog.alert("Đã thêm thành công");
        }
    }

    @Override
    public void update() {
        if (UDialog.confirm("Bạn thực sự muốn cập nhật thông tin này?")) {
            HoaDon hd = this.getForm();
            dao.update(hd);
            this.fillToTable();
            this.clear();
            UDialog.alert("Cập nhật thành công!");
        }
    }

    @Override
    public void delete() {
        int row = tblhoadon.getSelectedRow();
    if (row == -1) {
        UDialog.alert("Vui lòng chọn hóa đơn cần xóa!");
        return;
    }

    String maNV = tblhoadon.getValueAt(row, 0).toString();
    if (UDialog.confirm("Bạn chắc chắn muốn xóa hóa đơn này?")) {
        dao.deleteById(maNV);
        this.fillToTable();
        this.clear();
        UDialog.alert("Xóa thành công!");
    }
    }

    @Override
    public void clear() {
        this.setForm(new HoaDon());
    }

    @Override
    public void moveFirst() {
        int currentIndex = tblhoadon.getSelectedRow();
        if (currentIndex == 0) {
            UDialog.alert( "Đã ở vị trí đầu tiên rồi!");
        } else {
            this.moveTo(0);
        }
    }

    @Override
    public void movePrevious() {
        this.moveTo(tblhoadon.getSelectedRow() - 1);
    }

    @Override
    public void moveNext() {
        this.moveTo(tblhoadon.getSelectedRow() + 1);
    }

    @Override
    public void moveLast() {
        this.moveTo(tblhoadon.getRowCount() - 1);
    }

    @Override
    public void moveTo(int rowIndex) {
        if (rowIndex < 0) {
        this.moveLast();
    } else if (rowIndex >= tblhoadon.getRowCount()) {
        this.moveFirst();
    } else {
        tblhoadon.clearSelection();
        tblhoadon.setRowSelectionInterval(rowIndex, rowIndex);
        this.edit();
    }
    }


