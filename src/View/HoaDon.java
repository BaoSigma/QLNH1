    @Override
    public void edit() {
        HoaDon entity = items.get(tblhoadon.getSelectedRow());
        this.setForm(entity);
    }

    @Override
    public void create() {
        if (!Checkall()) return; 

        if (UDialog.confirm("Bạn thực sự muốn thêm nhân viên này?")) {
            HoaDon entity = this.getForm();
            dao.create(entity);
            this.fillToTable();
            this.clear();
            UDialog.alert("Đã thêm thành công");
        }
    }

    @Override
    public void update() {
        if (!Checkall()) return;

        if (UDialog.confirm("Bạn thực sự muốn cập nhật thông tin nhân viên này?")) {
            HoaDon hd = this.getForm();
            dao.update(hd);
            this.fillToTable();
            this.clear();
            UDialog.alert("Cập nhật thành công!");
        }
    }

    @Override
    public void delete() {
        int row = tblhoadon.getSelectedRow();
    if (row == -1) {
        UDialog.alert("Vui lòng chọn nhân viên cần xóa!");
        return;
    }

    String maNV = tblhoadon.getValueAt(row, 0).toString();
    if (UDialog.confirm("Bạn chắc chắn muốn xóa nhân viên này?")) {
        dao.deleteById(maNV);
        this.fillToTable();
        this.clear();
        UDialog.alert("Xóa nhân viên thành công!");
    }
    }

    @Override
    public void clear() {
        this.setForm(new HoaDon());
    }

    @Override
    public void moveFirst() {
        int currentIndex = tblhoadon.getSelectedRow();
        if (currentIndex == 0) {
            UDialog.alert( "Đã ở vị trí đầu tiên rồi!");
        } else {
            this.moveTo(0);
        }
    }

    @Override
    public void movePrevious() {
        this.moveTo(tblhoadon.getSelectedRow() - 1);
    }

    @Override
    public void moveNext() {
        this.moveTo(tblhoadon.getSelectedRow() + 1);
    }

    @Override
    public void moveLast() {
        this.moveTo(tblhoadon.getRowCount() - 1);
    }

    @Override
    public void moveTo(int rowIndex) {
        if (rowIndex < 0) {
        this.moveLast();
    } else if (rowIndex >= tblhoadon.getRowCount()) {
        this.moveFirst();
    } else {
        tblhoadon.clearSelection();
        tblhoadon.setRowSelectionInterval(rowIndex, rowIndex);
        this.edit();
    }
    }

